const useBezierPath = (sx, sy, tx, ty) => {
  const [path, setPath] = useState('');
  useEffect(() => {
    // Offset by roughly half the node width (62.5) and height (30) to anchor at center
    const update = () => {
      const x1 = sx.get() + 62.5;
      const y1 = sy.get() + 30;
      const x2 = tx.get() + 62.5;
      const y2 = ty.get() + 30;
      setPath(`M ${x1} ${y1} C ${(x1+x2)/2} ${y1}, ${(x1+x2)/2} ${y2}, ${x2} ${y2}`);
    };
    update();
    const us1 = sx.on("change", update);
    const uy1 = sy.on("change", update);
    const us2 = tx.on("change", update);
    const uy2 = ty.on("change", update);
    return () => { us1(); uy1(); us2(); uy2(); };
  }, [sx, sy, tx, ty]);
  return path;
};

const ThreadPath = ({ sx, sy, tx, ty, className }) => {
  const path = useBezierPath(sx, sy, tx, ty);
  return <path d={path} className={className} />;
};

